void drawScene(glm::mat4 & view , glm::mat4 & projection, glm::vec3& cameraPos, unsigned int reflectProbe)
{
    glEnable(GL_DEPTH_TEST);
    glClearColor(0.1f, 0.1f, 0.1f, 1.0f);
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
    
    
    {
        // 渲染模型
        p_modelShader->use();
     
        glm::mat4 model = glm::mat4(1.0f);
        p_modelShader->setMat4("model",     model);
        p_modelShader->setMat4("view",       view);
        p_modelShader->setMat4("projection", projection);
        
        p_modelShader->setVec3("cameraPos",  cameraPos); // 传入相机的位置
        p_modelShader->setVec3("light.direction", 0.0f, 0.0f, -1.0f); // 天空盒的 阳光在后面，可能背光
        // modelShader.setVec3("light.direction", 0.0f, 0.0f, 1.0f);     // 为了好看点 这里加了一簇光在前面，跟天空盒的太阳不太一样
        p_modelShader->setInt("skybox", 4); // 注意修改纹理单元
        
        // 模型中部分mesh只有3个纹理
        // 模型加载器本身就已经在着色器中占用了4个纹理单元了，需要将天空盒绑定到第4个纹理单元上
        glActiveTexture(GL_TEXTURE0 + 4);  // ourModel
        glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
        p_ourModel->Draw(*p_modelShader);
    }
 
    if (reflectProbe != 0)
    {
        // 只有动态环境映射纹理 非空，才画盒子
        // 相当于渲染动态环境纹理的时候 ，不画盒子
        
        p_cubeShader->use();
        glm::mat4 model = glm::mat4(1.0f);
        p_cubeShader->setMat4("model",      model);
        p_cubeShader->setMat4("view",       view);
        p_cubeShader->setMat4("projection", projection);
        p_cubeShader->setVec3("cameraPos",  cameraPos); // 传入相机的位置
        p_cubeShader->setInt ("skybox",     0);
        // cubes
        glBindVertexArray(cubeVAO);
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_CUBE_MAP, reflectProbe); // 使用动态环境映射纹理 (这里纹理不包含二次反射，自己不会再别人的反射上)
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);
    }


    {
        // 最后渲染天空盒
        glDepthFunc(GL_LEQUAL);  // change depth function so depth test passes when values are equal to depth buffer's content
        p_skyboxShader->use();
        // 没有 model
        p_skyboxShader->setMat4("view", glm::mat4(glm::mat3(view)));// 天空盒把view矩阵的位移去掉
        p_skyboxShader->setMat4("projection", projection);
        p_skyboxShader->setInt("skybox", 0);
        // skybox cube
        glBindVertexArray(skyboxVAO);
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);
        glDepthFunc(GL_LESS); // set depth function back to default
    }
    
    
}

