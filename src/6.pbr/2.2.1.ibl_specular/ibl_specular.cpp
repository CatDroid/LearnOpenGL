#define CHECK_ERROR { auto __error = glGetError(); if (__error != GL_NO_ERROR) printf("gl Error %d at line %d\n", __error, __LINE__); }


bool g_ShowBRDFLut = false;   // IBL间接光光照 - 镜面反射部分 - BRDF 2D-LUT图
